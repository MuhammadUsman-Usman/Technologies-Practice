// shift() function
let random_Numbers = [3,5,8,9,10];
/*
 * ye function yey karay ga k array may first index
 * say aik element delete kar k return b us element ko 
 * karay ga.
 * jesy hi aik element first index say delete hoga
 * wesy hi baqi k elements left shift hun gay 
 * aik dosray k lay haaz say. 
 */
console.log(random_Numbers.shift());


// unshift() function
/*
 *  yey function yey karay ga k array elements
 * may first index par aik element add kar k 
 * return karday ga array ki new length. 
 */
console.log(random_Numbers.unshift(2));

